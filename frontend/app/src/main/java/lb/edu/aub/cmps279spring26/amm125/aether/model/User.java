package lb.edu.aub.cmps279spring26.amm125.aether.model;

public class User {
    private String id;
    private String name;
    private String username;
    private String email;

    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getUsername() {
        return username;
    }

    public String getEmail() {
        return email;
    }
}
